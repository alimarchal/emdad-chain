    •	يلتزم طالب الخدمة بالإستلام في الوقت المحدد ويحق لمزود الخدمة المغادرة بالخدمة او المنتج في حال تجاوز التأخير ١٥ دقيقة .<br>
    •	يلتزم مزود الخدمة بمسؤولية اكتمال عملية التسليم واعتماد استلام الفاتورة قبل مغادرة مكان التسليم وذلك الكترونياً عن طريق رقم الفاتوره او المسح الضوئي على تطبيق (Emdad Tools) بإستخدام الهواتف الذكية .<br>
    •	يلتزم مزود الخدمة بتحمل تكاليف الشحن في حال تم استخدام احد مزودين خدمات الشحن المتعاقدة مع المنصة ويتم ذلك بادراجها في الفاتورة النهائيه بين مزود الخدمة ومنصة امداد  .
