<p class="text-justify m-2 p-2">
    <strong>الضوابط والشروط والأحكام لمستخدم منصة إمداد:</strong> إن أي استخدام من قبلك للخدمات التي توفرها شركة منصة الإمداد التقنية يشكل موافقة منك على هذا العقد وأحكامه، وتبعاً لذلك يجب عليك عدم استخدام المنصة في حال لم تكن موافقاً على الأحكام والشروط الواردة في هذا العقد.
    تحتفظ شركة منصة الإمداد التقنية بحق تعديل أو تغيير هذه الأحكام والشروط دون إخطار مسبق، ويكون من مسؤوليتك كمستخدم للمنصة مراجعة ضوابط وشروط الاستخدام بشكل دوري لمعرفة التحديثات التي تطرأ على الأحكام والشروط، من خلال زيارة الشروط والاحكام في المنصة او من خلال استلام بريد الكتروني من فريق منصة امداد .
</p>
<p class="text-justify m-2 p-2">
    <strong>إقرار مزود الخدمة وطالب الخدمة :</strong> يقر ويوافق طالب الخدمة ومزود الخدمة أن إستخدامه للمنصة يعتبر إقرار صريح وواضح بقيامه بقراءة وفهم بنود وأحكام هذه الاتفاقية و طريقة إستخدام المنصة أو التطبيق أو الموقع، كما أنه يعتبر موافقة صريحة منه على قبول شروط وأحكام هذه الاتفاقية والاستجابة لها وهو بكامل الأهلية المعتبرة شرعاً وقانوناً.
    ومن المعلوم بأنه وفي حال إختيار الموافقة ، فإن أحكام وشروط الاتفاقية تعتبر سارية المفعول بين شركة منصة الإمداد التقنية وبين طالب الخدمة أو مزود الخدمة ، كما أن جميع الإلتزامات والشروط والأحكام في هذه الاتفاقية يلتزم بها طالب الخدمة ومزود الخدمة مالم يقتضي السياق غير ذلك.
</p>
