    •	يحق لكلٍ من ( مزود الخدمة – طالب الخدمة ) إنهاء التعاقد في حال لم يلتزم أحد الأطراف بالنصوص والأحكام والشروط والمتفق عليها في العقد، وذلك بإرسال طلب لإلغاء التعاقد لـشركة منصة الإمداد التقنية عن طريق أيقونة ( فسخ العقد ) مع إرفاق مايثبت ذلك، وسيقوم فريق منصة امداد بمراجعة الطلب وإلغائة بعد التأكد من ذلك.<br>
    •	يحق لكلٍ من ( مزود الخدمة – طالب الخدمة ) إنهاء العقد في أي وقت في حال موافقة كلا الطرفين على ذلك.
</p>
<p class="text-justify m-2 p-2">
