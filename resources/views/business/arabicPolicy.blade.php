<p class="font-bold text-2xl text-center"><strong>الشروط والأحكام للشراكة والتعاقد مع شركة منصة الإمداد التقنية</strong></p>

<p class="font-bold m-2 p-2"><strong>مقدمة</strong></p>
<p class="text-justify m-2 p-2">
    منصة الإمداد هي منصة الكترونية تقنية تم تصميمها بغرض تسهيل العمليات بين طالب الخدمة ومزود الخدمة ، لتسهيل عملياتهم وتقديم الخدمات لهم بشكل أفضل وأسرع ، من خلال التسجيل بالمنصة طبقاً للشروط والاحكام التي سيتم ذكرها فيما بعد ، حيث أن عمل المنصة يتلخص بقيام طالب الخدمة برفع الطلب عن طريق المنصة بعد كتابة معلومات الطلب وعليه سوف يتم استلام عرض أوعدة عروض اسعار من مزودي الخدمات ، وعند استلام العروض من مزودي الخدمات يقوم طالب الخدمة باختيار مزود الخدمة الذي يرغبه بناءً على رغباته من ناحيه السعر أو الجودة أو غير ذلك ، وبعد الاختيار والإعتماد عن طريق المنصة لمزود الخدمة المناسب يقوم مزود الخدمة بعدها بالتوريد كما هو متفق عليه في أمر الشراء ، علماً بأن منافذ الدفع المتاحه تكون إما بالآجل أو الدفع المسبق أو الدفع عند الاستلام ، وتكون عن طريق استخدام البطاقة الإئتمانية ( مدى – فيزا – أبل بي ) أو التحويل البنكي المباشر ، علماً بأن المدفوعات الماليه لمزود الخدمة تكون في الحساب البنكي لشركة منصة الإمداد التقنية وبعد التأكد من المعلومات وعملية التوريد والاستلام يتم تحويلها إلى مزود الخدمة مباشرةً ، كما أنه يقر طالب الخدمة ومزود الخدمة  أن شركة منصة الإمداد التقنية هي وسيط فقط لتقديم خدمة التواصل المباشر بين طالب الخدمة ومزود الخدمة بغرض التسهييل والتسريع والآمان لعمليات سلاسل الامداد ، نجاحكم هو نجاحنا.
</p>

<p class="text-justify m-2 p-2">
    يقصد بالعبارات والمصطلحات التالية أينما وردت في هذه الإتفاقية المعاني الموضحة أمام كل منها، مالم يقتض السياق غير ذلك.
</p>

<p class="text-justify m-2 p-2">
    <strong>شركة منصة الإمداد التقنية  (Emdad Platform):</strong> أضخم منصة الكترونية بنيت بخبرة عالية لتخدم البائع والمشتري في عمليات الشراء والبيع والتوريد والتخزين، بأقل التكاليف وأفضل المعايير التقنية واللوجستية.
</p>
<p class="text-justify m-2 p-2">
    <strong>تطبيق أو موقع أو منصة:</strong> قاعدة من البيانات أو العمليات البرمجية التي تمت برمجتها بالكامل عن طريق شركة منصة الإمداد التقنية بأفضل معايير الجودة التقنية التي تتيح لمستخميها التعامل بـ ( الخبرة - السرعة – السهولة – الأمان ) .
</p>
<p class="text-justify m-2 p-2">
    <strong>تطبيق اضافي:</strong> هو تطبيق يخدم المنصة (EmdadTools) يتم تحميله على أجهزة الجوال أو الأجهزة الذكية للإستفادة من خدمات تتبع الشحنات أو الإعتماد الإلكتروني وهي خدمة مجانية لمستخدمين المنصة ، ويتم استخدام نفس معلومات وبيانات الدخول للمنصة .
</p>
<p class="text-justify m-2 p-2">
    <strong>طالب الخدمة (المشتري):</strong> التسجيل في المنصة بغرض الاستفادة من خدمات المنصة لعمل الطلبات ومتابعة الشحنات واستلام الطلبات.
</p>
<p class="text-justify m-2 p-2">
    <strong>مزود الخدمة (البائع):</strong> التسجيل في المنصة بغرض الاستفادة من خدمات المنصة لعمل عروض الأسعار وجدولة الشحنات والتوريد والتسليم وتقديم الخدمة المتميزة.
</p>
