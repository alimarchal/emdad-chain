    •	طرف مستقل ولا تعمل كوكيل أو ممثل أو موزع أو شريك استثماري لـشركة منصة الإمداد التقنية.<br>
    •	لم يسبق أن تم تعطيل استخدامك لخدمات "شركة منصة الإمداد التقنية" أو منعك من استخدامها في أي وقت من الأوقات.<br>
    •	لست منافساً لـشركة منصة الإمداد التقنية ، كما أنك لا تقدم أي منتج منافس للخدمات المقدمة من شركة منصة الإمداد التقنية.<br>
    •	لا يجوز لك بشكل مباشر أو غير مباشر تقديم نفسك كممثل لـشركة منصة الإمداد التقنية أو التصرف بطريقة قد تحمل أو تنشأ منها التزامات دون موافقة كتابية مسبقة من شركة منصة الإمداد التقنية.<br>
    •	إذا تم منح الموافقة حدود تصرفك تقتصر على الأعمال التي تسمح لك بتنفيذ الخدمة المقدمة فقط ، كما أنه ليس لك أي حق في تقديم أي عروض للمستخدمين نيابة عن شركة منصة الإمداد التقنية ، وأن تقر بأن خرقك لهذا البند سوف يجعلك مسؤولاً مسؤولية كاملة عن جميع الإلتزامات الناشئة عن هذا الخرق ويعرضك للملاحقة الجنائية وحظر استخدامك لـشركة منصة الإمداد التقنية.<br>
