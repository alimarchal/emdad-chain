    •	 يحق لطالب الخدمة إلغاء الطلب واسترداد المبلغ المدفوع في حال كان الدفع مسبقاً ويكون ذلك بسبب التأخر عن الموعد المحدد للتوريد بـ ٣ أيام من التاريخ المحدد في أمر الشراء المعتمد .
</p>
<p class="text-justify m-2 p-2">
