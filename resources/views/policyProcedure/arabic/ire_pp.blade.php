    ２-	التخاطب مع العميل بشكل غير لائق ومحترم أو رفع نبرة الصوت ويتم هنا إنهاء التعاقد مباشرةً.<br>
    ３-	استخدام المنصب في أعمال خارج المطلوب منه وذلك ينهي التعاقد مباشرةً.<br>
    ４-	التوقف عن تحديث البيانات أو التفاعل عن طريق المنصة لأكثر من ٣ أشهر.<br>
    ５-	عند انتهاء صلاحية الهوية الوطنية او الإقامة او أحد المستندات الرسمية خلال فترة أقصها أسبوع لتحديث البيانات في المنصة.<br>
    ６-	إذا تم الكشف عن معلومات مسجلة غير صحيحة أو غير مطابقة.
</p>

<p class="font-bold m-2 p-2"><strong><u>أمان الحساب وخصوصية المعلومات:</u></strong></p>
<p class="text-justify m-2 p-2">
