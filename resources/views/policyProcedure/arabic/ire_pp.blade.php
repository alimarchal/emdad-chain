<p class="font-bold text-2xl text-center"><strong>شروط وأحكام ممثل المبيعات الحُر:</strong></p>

<p class="font-bold m-2 p-2"><strong><u>التعريف:</u></strong></p>
<p class="text-justify m-2 p-2">
    فيما يلي شروط وأحكام ممثل المبيعات الحُر المتعاقد مع شركة منصة الإمداد التقنية وذلك للتسويق وإقناع أكبر عدد من العملاء (المستهدفين) للتسجيل واستخدام المنصة. ويكون للبائع الحر عمولة لكل منشأة جديدة تقوم بالتسجيل عن طريقه، ويكون ذلك بشروط وأحكام تفرضها شركة منصة الإمداد التقنية والتي باعتبارها هنا (الطرف الأول) ويكون (الطرف الثاني) هو ممثل المبيعات الحُر.
</p>

<p class="font-bold m-2 p-2"><strong><u>الضوابط والشروط والأحكام لمستخدم منصة إمداد:</u></strong></p>
<p class="text-justify m-2 p-2">
    إن أي استخدام من قبلك للخدمات التي توفرها شركة منصة الإمداد التقنية يشكل موافقة منك على هذا العقد وأحكامه، وتبعاً لذلك يجب عليك عدم استخدام المنصة في حال لم تكن موافقاً على الأحكام والشروط الواردة في هذا العقد.
    تحتفظ شركة منصة الإمداد التقنية بحق تعديل أو تغيير هذه الأحكام والشروط دون إخطار مسبق، ويكون من مسؤوليتك كمستخدم للمنصة مراجعة ضوابط وشروط الاستخدام بشكل دوري لمعرفة التحديثات التي تطرأ على الأحكام والشروط، من خلال زيارة صفحة الشروط والأحكام في المنصة أو من خلال استلام بريد إلكتروني من فريق منصة إمداد.
</p>

<p class="font-bold m-2 p-2"><strong><u>العملاء المستهدفين للبائع الحر:</u></strong></p>
<p class="text-justify m-2 p-2">
    أي منشأة صغيرة او متوسطة أو كبيرة لديها سجل تجاري ساري الصلاحية ومقر رسمي واضح ولديها طلبات لمشتريات مستمرة طول العام، ومثالاً على ذلك (المطاعم – المقاهي – المستشفيات – الصيدليات – المجمعات التجارية – الفنادق والشقق إلخ).
</p>

<p class="font-bold m-2 p-2"><strong><u>طريقة التسويق المطلوبة:</u> </strong></p>
<p class="text-justify m-2 p-2">
    １-	اختيار المنشأة والتعرف عليها عن قرب ومثالاً على ذلك (تاريخ المنشأة بشكل بسيط – عدد الفروع – أصعب التحديات التي تواجهها)<br>
    ２-	اختيار الموظف المناسب في المنشأة للتعريف بالمنصة<br>
    ３-	تحديد الوقت الذي يناسب الموظف<br>
    ４-	الحضور قبل الموعد ب ١٠ دقائق<br>
    ５-	التعريف بالاسم بطريقة لبقة ومحترمة<br>
    ６-	التعريف عن منصة إمداد بثقة كاملة<br>
    ７-	إقناع الموظف لتسجيل منشأته<br>
    ８-	المتابعة بعد ذلك بالطريقة الصحيحة<br>
    ９-	دعم المنشأة دعماً كاملاً بعد التسجيل ولا يجوز التقصير في ذلك<br>
</p>


<p class="font-bold m-2 p-2"><strong><u>العمولات:</u></strong></p>
<p class="text-justify m-2 p-2">
