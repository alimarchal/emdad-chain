    •	كما أنك توافق على إخطار " شركة منصة الإمداد التقنية" إذا أصبحت على علم بأي خسارة أو سرقة أو استخدام غير مصرح به لأي كلمة مرور، أو اسم مستخدم أو عنوان بروتكول الانترنت IP، أو غير ذلك من أساليب الوصول إلى معلومات الحساب.
</p>

<p class="font-bold m-2 p-2"><strong><u>التعاملات خارج المنصة:</u></strong></p>
<p class="text-justify m-2 p-2">
