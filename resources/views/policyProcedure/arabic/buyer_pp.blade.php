    •	يحق لـ"شركة منصة الإمداد التقنية" رفض تقديم الخدمة أو استخدام المنصة دون إبداء أسباب.<br>
    •	إخلاء مسؤولية "شركة منصة الإمداد التقنية" عن أي استخدام خاطئ من قبل المستخدمين المعتمدين لدينا رسمياً.
