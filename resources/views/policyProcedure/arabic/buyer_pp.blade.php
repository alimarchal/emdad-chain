    •	يحق لطالب الخدمة إلغاء الطلب واسترداد المبلغ المدفوع في حال الدفع المسبق، ويكون ذلك بسبب التأخر عن الموعد المحدد للتوريد بـ ٣ أيام من التاريخ المحدد في أمر الشراء المعتمد.
</p>

<p class="font-bold m-2 p-2"><strong>أوامر الشراء المرفق لها صيغة للتعاقد:</strong></p>
<p class="text-justify m-2 p-2">
    •	يلتزم كلاً من (مزود الخدمة - طالب الخدمة) بجميع النصوص والأحكام والشروط المكتوبة في العقد المرفق فيما بينهما ولا يمكن تعديلها إلا بموافقة الطرفين بطلب التعديل عن طريق المنصة وذلك باستخدام أيقونة (طلب تعديل صيغة العقد) وفي حال موافقة الطرف الآخر يتم التعديل والموافقة من الطرفين واعتماده مرة اخرى.<br>
    •	يحق لكلٍ من (مزود الخدمة - طالب الخدمة) إنهاء التعاقد في حال لم يلتزم أحد الأطراف بالنصوص والأحكام والشروط والمتفق عليها في العقد. وذلك بإرسال طلب لإلغاء التعاقد إلى شركة منصة الإمداد التقنية عن طريق أيقونة (فسخ العقد) مع إرفاق ما يثبت ذلك، وسيقوم فريق منصة إمداد بمراجعة الطلب وإلغائه بعد التأكد من ذلك.<br>
    •	يحق لكلٍ من (مزود الخدمة - طالب الخدمة) إنهاء العقد في أي وقت في حال موافقة كلا الطرفين على ذلك.
</p>

