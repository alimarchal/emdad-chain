    •	يلتزم طالب الخدمة باستلام الخدمة أو الشحنة المعتمدة إذا كانت مطابقة للمواصفات المعتمدة في أمر الشراء، مالم تتجاوز التاريخ المتفق عليه بـ ٣ أيام.<br>
    •	يلتزم طالب الخدمة بالاستلام في الوقت المحدد ويحق لمزود الخدمة المغادرة دون تقديم الخدمة أو المنتج في حال تجاوز التأخير ١٥ دقيقة.<br>
    •	يلتزم مزود الخدمة بمسؤولية اكتمال عملية التسليم واعتماد استلام الفاتورة قبل مغادرة مكان التسليم ويتم ذلك إلكترونياً عن طريق رقم الفاتورة أو المسح الضوئي على تطبيق (Emdad Tools) باستخدام الهواتف الذكية.<br>
    •	يلتزم مزود الخدمة بتحمل تكاليف الشحن في حال تم استخدام أحد مزودي خدمات الشحن المتعاقدة مع المنصة ويتم ذلك بإدراجها في الفاتورة النهائية بين مزود الخدمة ومنصة إمداد.
</p>

<p class="font-bold m-2 p-2"><strong>حقوق النشر والحقوق الفكرية الخاصة بـشركة منصة الإمداد التقنية:</strong></p>
<p class="text-justify m-2 p-2">
