<p class="font-bold text-2xl text-center" style="text-align: center"><strong>الشروط والأحكام للشراكة والتعاقد مع شركة منصة الإمداد التقنية</strong></p>

<p class="font-bold m-2 p-2"><strong>مقدمة</strong></p>
<p class="text-justify m-2 p-2">
    منصة الإمداد هي منصة إلكترونية تقنية تم تصميمها بغرض تسهيل العمليات بين طالب الخدمة ومزود الخدمة ، لتسهيل عملياتهم وتقديم الخدمات لهم بشكل أفضل وأسرع ، من خلال التسجيل بالمنصة طبقاً للشروط والأحكام التي سيتم ذكرها فيما بعد ، حيث أن عمل المنصة يتلخص بقيام طالب الخدمة برفع الطلب عن طريق المنصة بعد كتابة معلومات الطلب وعليه سوف يتم استلام عرض أو عدة عروض أسعار من مزودي الخدمات ، وعند استلام العروض من مزودي الخدمات يقوم طالب الخدمة باختيار عرض السعر الذي يرغب به بناءً على رغباته من ناحية السعر أو الجودة أو غير ذلك ، وبعد الاختيار والاعتماد عن طريق المنصة لعرض السعر المناسب يقوم مزود الخدمة بعدها بالتوريد كما هو متفق عليه في أمر الشراء ، علماً بأن منافذ الدفع المتاحة تكون إما بالآجل أو الدفع المسبق أو الدفع عند الاستلام ، وتكون عن طريق استخدام البطاقة الائتمانية ( مدى – فيزا – أبل بي ) أو التحويل البنكي المباشر ، علماً بأن المدفوعات المالية لمزود الخدمة تكون في الحساب البنكي لشركة منصة الإمداد التقنية وبعد التأكد من المعلومات وعملية التوريد والاستلام يتم تحويلها إلى مزود الخدمة مباشرةً ، كما أنه يقر كلاً من طالب الخدمة ومزود الخدمة  أن شركة منصة الإمداد التقنية هي وسيط فقط لتقديم خدمة التواصل المباشر بين طالب الخدمة ومزود الخدمة بغرض التسهيل والتسريع والأمان لعمليات سلاسل الإمداد، نجاحكم هو نجاحنا.
