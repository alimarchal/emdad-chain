<p class="font-bold m-2 p-2"><strong>الضوابط والشروط والأحكام لمستخدم منصة إمداد:</strong></p>
<p class="text-justify m-2 p-2">
    إن أي استخدام من قبلك للخدمات التي توفرها شركة منصة الإمداد التقنية يشكل موافقة منك على هذا العقد وأحكامه، وتبعاً لذلك يجب عليك عدم استخدام المنصة في حال لم تكن موافقاً على الأحكام والشروط الواردة في هذا العقد.
    تحتفظ شركة منصة الإمداد التقنية بحق تعديل أو تغيير هذه الأحكام والشروط دون إخطار مسبق، ويكون من مسؤوليتك كمستخدم للمنصة مراجعة ضوابط وشروط الاستخدام بشكل دوري لمعرفة التحديثات التي تطرأ على الأحكام والشروط، من خلال زيارة صفحة الشروط والأحكام في المنصة أو من خلال استلام بريد إلكتروني من فريق منصة إمداد.
</p>

<p class="font-bold m-2 p-2"><strong>إقرار طالب الخدمة:</strong></p>
<p class="text-justify m-2 p-2">
    يقر ويوافق طالب الخدمة أن استخدامه للمنصة يعتبر إقرار صريح وواضح بقيامه بقراءة وفهم بنود وأحكام هذه الاتفاقية وطريقة استخدام المنصة أو التطبيق أو الموقع، كما أنه يعتبر موافقة صريحة منه على قبول شروط وأحكام هذه الاتفاقية والاستجابة لها وهو بكامل الأهلية المعتبرة شرعاً وقانوناً.
    ومن المعلوم بأنه وفي حال الموافقة، فإن أحكام وشروط الاتفاقية تعتبر سارية المفعول بين شركة منصة الإمداد التقنية وبين طالب الخدمة، كما أن جميع الالتزامات والشروط والأحكام في هذه الاتفاقية يلتزم بها طالب الخدمة مالم يقتضي السياق غير ذلك.
</p>

<p class="font-bold m-2 p-2"><strong>أحقية الحصول على الخدمات:</strong></p>
<p class="text-justify m-2 p-2">
