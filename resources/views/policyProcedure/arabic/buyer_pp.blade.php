    •	كافة حقوق النشر والحقوق الفكرية محفوظة لـشركة منصة الإمداد التقنية، ويمنع منعاً باتاً انتهاك حقوق شركة منصة الإمداد التقنية، وفي حال تم ذلك فإن المنتهِك سيعرض نفسه للملاحقة والمساءلة القانونية من قبل الجهات الرسمية.<br>
    •	يمنع منعاً باتاً نسخ أو لصق أو استخدام أو إعادة استخدام أي محتوى خاص بـشركة منصة الإمداد التقنية أو خدماتها التي تقدمها أو أي شيء مملوك لـشركة منصة الإمداد التقنية.
