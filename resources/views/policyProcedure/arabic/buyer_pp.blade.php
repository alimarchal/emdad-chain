    •	يجب أن يوافق طالب الخدمة على أحقية شركة منصة الإمداد التقنية في إجراء تعديلات أو تغييرات في صفحة شروط الاستخدام الخاصة بها في أي وقت إذا رأت منصة امداد ضرورة لفعل ذلك <br>
    •	يمنع تماماً استخدام أي خدمات مقدمة من قبل شركة منصة الإمداد التقنية لأي أغراض غير قانونية أو غير أخلاقية أو أي أمور تتعارض مع الشريعة الإسلامية.
</p>

<p class="font-bold m-2 p-2"><strong>التعهدات والضمانات:</strong></p>
